    }

    if ( preg_match( '#^https?://#i', $value ) ) {
        return $value;
    }

    if ( 0 === strpos( $value, '/' ) ) {
        return home_url( $value );
    }

    return home_url( '/' . ltrim( $value, '/' ) );
}

