function dv_sanitize_theme_content( $input ) {
    $defaults = dv_get_theme_content_defaults();
    $input    = is_array( $input ) ? $input : array();
    $result   = array();

    $checkbox_keys = array(
        'home_popular_enabled',
        'home_sale_enabled',
        'home_categories_enabled',
    );

    for ( $i = 1; $i <= 5; $i++ ) {
        $checkbox_keys[] = 'custom_page_' . $i . '_enabled';
        $checkbox_keys[] = 'custom_page_' . $i . '_footer_enabled';
    }

    $number_keys = array(
        'home_popular_limit',
        'home_popular_order',
        'home_sale_limit',
        'home_sale_order',
        'home_categories_limit',
        'home_categories_order',
    );

    foreach ( $defaults as $key => $default ) {
        if ( in_array( $key, $checkbox_keys, true ) ) {
            $result[ $key ] = empty( $input[ $key ] ) ? '0' : '1';
            continue;
        }

        if ( in_array( $key, $number_keys, true ) ) {
            $value          = isset( $input[ $key ] ) ? absint( $input[ $key ] ) : (int) $default;
            $result[ $key ] = max( 1, $value );
            continue;
        }

        $value = $input[ $key ] ?? $default;
        if ( preg_match( '/^custom_page_\d+_slug$/', (string) $key ) ) {
            $result[ $key ] = sanitize_title( $value );
        } elseif ( preg_match( '/^custom_page_\d+_card_\d+_icon$/', (string) $key ) ) {
            $icon_options   = dv_theme_content_card_icon_options();
            $clean_icon     = sanitize_key( $value );
            $result[ $key ] = isset( $icon_options[ $clean_icon ] ) ? $clean_icon : (string) $default;
        } elseif ( false !== strpos( $key, '_url' ) ) {
            $result[ $key ] = esc_url_raw( $value );
        } else {
            $result[ $key ] = sanitize_textarea_field( $value );
        }
    }

    return $result;
}

