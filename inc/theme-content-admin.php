function dv_handle_theme_content_save() {
    if ( ! current_user_can( 'manage_options' ) ) {
        wp_die( esc_html__( 'Sorry, you are not allowed to manage these settings.', 'default' ) );
    }

    check_admin_referer( 'dv_save_theme_content', 'dv_theme_content_nonce' );

    $input = isset( $_POST['dv_theme_content'] ) ? wp_unslash( $_POST['dv_theme_content'] ) : array();
    $value = dv_sanitize_theme_content( $input );

    update_option( 'dv_theme_content', $value, false );

    $redirect = add_query_arg(
        array(
            'page'     => 'dv-theme-content',
            'dv_saved' => '1',
        ),
        admin_url( 'admin.php' )
    );

    wp_safe_redirect( $redirect );
    exit;
}
